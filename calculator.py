#defining function to multiply 
def multiply(a,b): 
	return a*b 

#defining function to add 
def add(a,b):
	return a+b

#defining function to subtract 
def subtract(a,b):
	return a-b

#defining function to divide 
def divide (a,b):
	return a/b 

